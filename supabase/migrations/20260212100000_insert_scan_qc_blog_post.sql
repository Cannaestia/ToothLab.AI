## What Is Scan QC?

Scan QC (Scan Quality Control) is a deliberate, step-by-step process to evaluate every digital impression before design begins. Instead of assuming the data is usable, we actively try to break it:

- Are all margins clearly visible 360°?
- Is there enough clearance for material and occlusal strength?
- Is the bite stable and repeatable across views?
- Are edentulous areas and soft tissues fully captured for partials and dentures?
- Is there distortion or "noise" in key areas like contact points and implant interfaces?

If a scan fails any of these checks, we don''t guess — we communicate.

